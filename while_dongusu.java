public class while_dongusu {
    public static void main(String[] args){
        for(int i =1; i<=10 ; i++){
            System.out.println(i);
        }
        System.out.println("For döngüsü bitmiştir. ");

        int i =1;
        while (i<5){
            System.out.println(i);
            i = i+2;
        }
        System.out.println("While döngüsü bitmiştir.");
    }
}
